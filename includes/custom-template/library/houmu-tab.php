            <h5>2. 契約書の検討依頼と捺印申請</h5>
            <div class="ml-55 mb-10">
                <p>【契約検討依頼が必要なケース】</p>
                <p class="ml-55">①新規ビジネス案件</p>
                <p class="ml-55">②上記契約書雛型以外の契約書を使用する場合</p>
                <p class="ml-55">③契約書雛型に相手方による修正があった場合</p>
                <p>【契約検討依頼から捺印申請までの流れ】</p>
                <div class="ml-55">
                    <p>①契約検討依頼案件が発生</p>
                    <p>②契約検討依頼に必要な書類を準備</p>
                    <div class="ml-30">
                        <p>・契約検討依頼書</p>
                        <p>・検討対象契約書</p>
                        <p>・関連契約書および資料等</p>
                    </div>
                    <p>③X-point「契約検討依頼書」による申請</p>
                    <div class="ml-30">
                        <p>※原契約は、捺印締結版のPDFファイルにて添付</p>
                        <p>※その他検討に必要な資料を添付</p>
                    </div>
                    <p>④法務担当による検討</p>
                    <div class="ml-30">
                        <p>※契約検討は、通常２週間程度かかります。</p>
                        <p class="ml-15">案件によっては更に時間を要する場合があるため、余裕をもって依頼するようにしてください。</p>
                    </div>
                    <p>⑤検討結果の返却</p>
                    <p class="ml-30">※返却された契約検討依頼書は捺印申請時の添付書類です</p>
                    <p>⑥部門でのリスクチェックおよび取引先との調整</p>
                    <div class="ml-30">
                        <p>※再度法務検討を要する場合は、X-point「契約再検討依頼書」により申請してください。</p>
                        <p class="ml-15">「契約再検討依頼書」は、元のX-point「契約検討依頼書」の関連書類から申請します。</p>
